#Diz respeito a escalabilidade, não velocidade


#O(1) -> Sempre vai possuir o mesmo tempo de execução mesmo que o input aumente de tamanho (constante)

#O(log n) -> Busca binaria (array ordenado), joga o ponteiro na metade do array buscando o target(input)

#O(n) -> Aumenta proporcionalmente o tempo de execução baseado no tamanho do input (Linear)

#O(n log n) -> SORT (algoritmos de ordenação) - Quicksort e Mergesort (dividir e conquistar)

#O(n²) -> Força Bruta (comparação de pares entre todos os valores)